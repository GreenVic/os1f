static inline bool timeout(const struct task *t)
{
  return (t->timeout_at <= HAL_GetTick());
}  

void SysTick_Handler(void)
{
  HAL_IncTick();
  //++systick;
  // is it time for the current task to turn over its timeslice?
  if (timeout(task_current()))
    SCB->ICSR |= SCB_ICSR_PENDSVSET_Msk;
}
